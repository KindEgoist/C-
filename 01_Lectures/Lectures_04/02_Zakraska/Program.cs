void Print(int[,] image)
{
    for(int i = 0; i < image.GetLength(0); i++)
    {
        for(int j = 0; j < image.GetLength(1); j++)
        {
        if (image[i,j] == 0) Console.Write($" ");
        else Console.Write($"#");
        }
    Console.WriteLine();
    }
}

void FillImage(int x, int y)
{
    if(pic[x, y] == 0)
    {
        pic[x, y] = 1;
        FillImage(x - 1, y);
        FillImage(x, y - 1);
        FillImage(x + 1, y);
        FillImage(x, y + 1);
    }
}

